package com.anonymouscorgi.karakoram.base;

/**
 * A class representing a node in a singly linked list.
 */
public class ListNode {

    public int val;
    public ListNode next;

    /**
     * Constructs a ListNode with the given value.
     */
    public ListNode(int val) {
        this.val = val;
    }
}
